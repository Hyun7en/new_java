package org.example.test;

public class Programmers {
    public static void main(String[] args) {
        String str = "Hello, World!";

        // 문자열을 char 배열로 변환
        char[] charArray = str.toCharArray();

        // char 배열 출력
        for (char c : charArray) {
            System.out.println(c);
        }

        //end
    }


}

