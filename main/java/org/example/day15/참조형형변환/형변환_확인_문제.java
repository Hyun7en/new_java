import java.util.ArrayList;

public class 형변환_확인_문제 {
    public static void main(String[] args) {
//        1. 형변환(캐스팅)이란?
/*
        형변환(캐스팅)은 변수의 타입을 다른 타입으로 변환하는 과정이다.
        기본 타입 간의 형변환과 객체 타입 간의 형변환이 가능하다.
        예를 들어, int를 double로 변환하거나, 부모 클래스 타입의 객체를 자식 클래스 타입으로 변환하는 경우 등이 있다.

 */


//        2. 업 캐스팅이란? 다른 말은?
/*
        업 캐스팅(Upcasting)은 자식 클래스의 객체를 부모 클래스 타입으로 변환하는 것입니다.
        이 과정에서 자식 클래스의 모든 특성을 사용할 수 없고,
        부모 클래스의 메서드와 변수를 사용할 수 있다.
        업 캐스팅은 안전하게 수행되며, 명시적인 형변환이 필요하지 않습니다.
        묵시적 형변환 또는 자동 형변환이라고 볼 수 있음 그러나 완전히 같은 말은 아님
        업 캐스팅은 객체간의 형변환에서만 쓰임

 */

//        3. 다운 캐스팅이란? 다른 말은?
/*
        다운 캐스팅(Downcasting)은 부모 클래스 타입의 객체를 자식 클래스 타입으로 변환하는 것입니다.
        이 과정에서는 부모 클래스에서 자식 클래스의 메서드와 변수를 사용할 수 있다.
        다운 캐스팅은 위험할 수 있으며, 명시적인 형변환이 필요하다.
        잘못된 다운 캐스팅은 ClassCastException을 발생시킬 수 있다.
        명시적 형변환 또는 강제 형변환이라고 볼 수 있음 그러나 완전히 같은 말은 아님
        다운 캐스팅은 객체간의 형변환에서만 쓰임
 */

//        4. Car ← Truck에서 부모 클래스/자식 클래스를 구분하시오.
//        Car(부모) Truck(자식)

//        5. byte b = 100; int i = b; 내부에서 일어난 처리 내용을 쓰시오.
//        byte b = 100;에서 b는 100을 저장하고, int i = b;에서 b는 int 타입으로 자동 변환된다.
//        내부적으로, byte는 8비트이고 int는 32비트이므로 b의 값이 i에 복사되면서 필요한 경우 자동으로 타입 변환이 이루어진다.
//        이 과정에서 데이터 손실은 없고, i는 100을 가지게 된다.

//        6. int i2 = 10; byte b2 = (byte) i2; 내부에서 일어난 처리 내용을 쓰시오.
/*
        int i2 = 10;에서 i2는 10을 저장하고 있다.
        이후 byte b2 = (byte) i2;에서 i2의 값을 byte 타입으로 변환하기 위해 명시적 형변환이 이루어진다.
        int는 32비트이고 byte는 8비트이므로, i2의 값인 10이 b2에 저장된다.
        이 과정에서 데이터 손실이 없지만, i2가 만약 byte의 범위를 초과하는 값이었다면 손실이 발생할 수 있다.
 */
//        7. 오토박싱이란?
        /*
        오토박싱(Autoboxing)은 기본 데이터 타입(예: int, double)을 해당하는 래퍼 클래스(예: Integer, Double)로 자동 변환하는 과정입니다.
        예를 들어, int 타입의 값을 Integer 객체로 저장할 때 자동으로 변환됩니다.
         */

//        8. 오토언박싱이란?
        /*
        오토언박싱(Unboxing)은 래퍼 클래스(예: Integer, Double) 객체를 해당하는 기본 데이터 타입(예: int, double)으로 자동 변환하는 과정입니다.
        예를 들어, Integer 객체에서 int 값을 추출할 때 자동으로 변환됩니다.
         */

//        9. 다음 각 줄에 주석을 달아주세요.
        ArrayList list = new ArrayList();  // ArrayList 객체를 생성하여 list에 할당
        list.add(100);                       // list에 Integer 값 100을 추가 (자동 박싱)
        int age = (Integer) list.get(0);    // list의 첫 번째 요소를 Integer로 가져와서 int 타입으로 변환



    }
}
